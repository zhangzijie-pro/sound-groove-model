def parse_args():
    parser = argparse.ArgumentParser(description="Full-recording diarization evaluation with RTTM export.")
    parser.add_argument("--ckpt", type=str, required=True)
    parser.add_argument(
        "--manifest",
        type=str,
        default="processed/real_diar_dataset/test_manifest.jsonl",
        help="Chunk manifest generated by processed/build_processed_dataset.py",
    )
    parser.add_argument("--out_dir", type=str, default="outputs_eval")
    parser.add_argument("--device", type=str, default="cuda")
    parser.add_argument("--chunk_sec", type=float, default=4.0)
    parser.add_argument("--hop_sec", type=float, default=2.0)
    parser.add_argument("--activity_threshold", type=float, default=0.5)
    parser.add_argument("--exist_threshold", type=float, default=0.5)
    parser.add_argument("--min_active_frames", type=int, default=3)
    parser.add_argument("--prototype_match_threshold", type=float, default=0.70)
    parser.add_argument("--prototype_momentum", type=float, default=0.8)
    return parser.parse_args()


def load_jsonl(path: str | Path) -> List[dict]:
    items: List[dict] = []
    with Path(path).open("r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if line:
                items.append(json.loads(line))
    return items


def parse_rttm(path: str | Path) -> List[Dict[str, Any]]:
    segments: List[Dict[str, Any]] = []
    with Path(path).open("r", encoding="utf-8") as f:
        for line in f:
            parts = line.strip().split()
            if len(parts) < 8 or parts[0] != "SPEAKER":
                continue
            start = float(parts[3])
            dur = float(parts[4])
            if dur <= 0:
                continue
            segments.append(
                {
                    "recording_id": parts[1],
                    "start": start,
                    "end": start + dur,
                    "speaker": parts[7],
                }
            )
    return segments


def build_model_from_ckpt(ckpt_path: str, device: torch.device):
    ckpt = torch.load(ckpt_path, map_location=device)
    cfg = ckpt.get("config", {})
    model_cfg = cfg.get("model", {})

    model = EENDQueryModel(
        in_channels=int(model_cfg.get("in_channels", 80)),
        enc_channels=int(model_cfg.get("channels", 512)),
        d_model=int(model_cfg.get("d_model", 256)),
        max_speakers=int(model_cfg.get("max_mix_speakers", 8)),
        decoder_type=str(model_cfg.get("decoder_type", "query")),
        post_ffn_hidden_dim=int(model_cfg.get("post_ffn_hidden_dim", 1024)),
        post_ffn_dropout=float(model_cfg.get("post_ffn_dropout", 0.1)),
        decoder_layers=int(model_cfg.get("decoder_layers", 4)),
        decoder_heads=int(model_cfg.get("decoder_heads", 8)),
        decoder_ffn=int(model_cfg.get("decoder_ffn", 1024)),
        dropout=float(model_cfg.get("dropout", 0.1)),
    ).to(device)

    model.load_state_dict(get_model_state_from_ckpt(ckpt), strict=True)
    model.eval()
    return model, cfg


def pad_chunk(feat: torch.Tensor, chunk_frames: int) -> Tuple[torch.Tensor, torch.Tensor]:
    valid_len = int(feat.size(0))
    if valid_len >= chunk_frames:
        return feat[:chunk_frames], torch.ones(chunk_frames, dtype=torch.bool)

    padded = torch.zeros(chunk_frames, feat.size(1), dtype=feat.dtype)
    padded[:valid_len] = feat
    valid_mask = torch.zeros(chunk_frames, dtype=torch.bool)
    valid_mask[:valid_len] = True
    return padded, valid_mask


def sliding_window_starts(total_frames: int, chunk_frames: int, hop_frames: int) -> List[int]:
    if total_frames <= chunk_frames:
        return [0]
    starts = list(range(0, total_frames - chunk_frames + 1, hop_frames))
    tail_start = total_frames - chunk_frames
    if starts[-1] != tail_start:
        starts.append(tail_start)
    return starts


def remove_short_runs(binary_seq: torch.Tensor, min_active_frames: int) -> torch.Tensor:
    out = binary_seq.clone()
    t = 0
    total = out.numel()
    while t < total:
        if float(out[t].item()) < 0.5:
            t += 1
            continue
        end = t + 1
        while end < total and float(out[end].item()) >= 0.5:
            end += 1
        if end - t < min_active_frames:
            out[t:end] = 0.0
        t = end
    return out


def decode_chunk(
    diar_logits: torch.Tensor,
    exist_logits: torch.Tensor,
    min_active_frames: int,
    activity_threshold: float,
    exist_threshold: float,
) -> Tuple[torch.Tensor, torch.Tensor]:
    diar_prob = torch.sigmoid(diar_logits)
    exist_prob = torch.sigmoid(exist_logits)
    keep_mask = exist_prob >= exist_threshold
    pred_bin = torch.zeros_like(diar_prob, dtype=torch.float32)

    for idx in torch.where(keep_mask)[0].tolist():
        pred_bin[:, idx] = (diar_prob[:, idx] >= activity_threshold).float()
        pred_bin[:, idx] = remove_short_runs(pred_bin[:, idx], min_active_frames=min_active_frames)

    return pred_bin, keep_mask


def match_active_attractors(
    active_attractors: torch.Tensor,
    prototypes: List[torch.Tensor],
    threshold: float,
    momentum: float,
) -> Tuple[List[int], List[torch.Tensor]]:
    if active_attractors.numel() == 0:
        return [], prototypes

    active_attractors = F.normalize(active_attractors, dim=-1)
    assigned: Dict[int, int] = {}
    used_queries = set()
    used_protos = set()

    if prototypes:
        proto_tensor = F.normalize(torch.stack(prototypes, dim=0), dim=-1)  # [P, D]
        sim = torch.matmul(active_attractors, proto_tensor.transpose(0, 1))  # [Q, P]

        flat_candidates = []
        for qi in range(sim.size(0)):
            for pi in range(sim.size(1)):
                flat_candidates.append((float(sim[qi, pi].item()), qi, pi))
        flat_candidates.sort(reverse=True, key=lambda x: x[0])

        for score, qi, pi in flat_candidates:
            if score < threshold:
                break
            if qi in used_queries or pi in used_protos:
                continue
            assigned[qi] = pi
            used_queries.add(qi)
            used_protos.add(pi)

    global_ids: List[int] = []
    for qi in range(active_attractors.size(0)):
        attr = active_attractors[qi]
        if qi in assigned:
            gid = assigned[qi]
            updated = momentum * prototypes[gid] + (1.0 - momentum) * attr.detach().cpu()
            prototypes[gid] = F.normalize(updated, dim=0)
        else:
            gid = len(prototypes)
            prototypes.append(attr.detach().cpu())
        global_ids.append(gid)

    return global_ids, prototypes


def ensure_track_capacity(track_matrix: torch.Tensor, num_tracks: int) -> torch.Tensor:
    if track_matrix.size(1) >= num_tracks:
        return track_matrix
    pad = torch.zeros(track_matrix.size(0), num_tracks - track_matrix.size(1), dtype=track_matrix.dtype)
    return torch.cat([track_matrix, pad], dim=1)


def frame_tracks_to_segments(
    track_matrix: torch.Tensor,
    merge_gap_frames: int = 10,
) -> List[Dict[str, Any]]:
    segments: List[Dict[str, Any]] = []
    if track_matrix.numel() == 0:
        return segments

    for speaker_id in range(track_matrix.size(1)):
        mask = track_matrix[:, speaker_id] > 0.5
        if not bool(mask.any().item()):
            continue

        mask = mask.clone()
        if merge_gap_frames > 0:
            t = 0
            total = mask.numel()
            while t < total:
                if bool(mask[t].item()):
                    t += 1
                    continue
                end = t + 1
                while end < total and not bool(mask[end].item()):
                    end += 1
                left_on = t > 0 and bool(mask[t - 1].item())
                right_on = end < total and bool(mask[end].item())
                if left_on and right_on and (end - t) <= merge_gap_frames:
                    mask[t:end] = True
                t = end

        t = 0
        total = mask.numel()
        while t < total:
            if not bool(mask[t].item()):
                t += 1
                continue
            end = t + 1
            while end < total and bool(mask[end].item()):
                end += 1
            segments.append(
                {
                    "start": t / 100.0,
                    "end": end / 100.0,
                    "speaker": f"spk{speaker_id:02d}",
                }
            )
            t = end

    segments.sort(key=lambda x: (x["start"], x["speaker"]))
    return segments


def write_rttm(path: str | Path, recording_id: str, segments: List[Dict[str, Any]]):
    path = Path(path)
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as f:
        for seg in segments:
            dur = max(0.0, float(seg["end"]) - float(seg["start"]))
            if dur <= 0:
                continue
            f.write(
                f"SPEAKER {recording_id} 1 {seg['start']:.3f} {dur:.3f} <NA> <NA> {seg['speaker']} <NA> <NA>\n"
            )


def build_annotation_from_segments(uri: str, segments: List[Dict[str, Any]]):
    from pyannote.core import Annotation, Segment

    ann = Annotation(uri=uri)
    for seg in segments:
        start = float(seg["start"])
        end = float(seg["end"])
        if end <= start:
            continue
        ann[Segment(start, end)] = str(seg["speaker"])
    return ann


def score_with_pyannote(uri: str, ref_segments: List[Dict[str, Any]], hyp_segments: List[Dict[str, Any]]):
    try:
        from pyannote.metrics.diarization import DiarizationErrorRate, JaccardErrorRate
    except Exception:
        return None

    ref_ann = build_annotation_from_segments(uri, ref_segments)
    hyp_ann = build_annotation_from_segments(uri, hyp_segments)

    der_metric = DiarizationErrorRate(collar=0.0, skip_overlap=False)
    jer_metric = JaccardErrorRate(collar=0.0, skip_overlap=False)

    der = float(der_metric(ref_ann, hyp_ann))
    jer = float(jer_metric(ref_ann, hyp_ann))
    return {"der": der, "jer": jer}


def group_recordings(manifest_items: List[dict]) -> List[dict]:
    grouped: Dict[Tuple[str, str], dict] = {}
    for item in manifest_items:
        key = (item["dataset"], item["recording_id"])
        grouped.setdefault(
            key,
            {
                "dataset": item["dataset"],
                "recording_id": item["recording_id"],
                "wav_path": item["wav_path"],
                "rttm_path": item["rttm_path"],
            },
        )
    return list(grouped.values())


@torch.no_grad()
def infer_recording(
    model,
    wav_path: str,
    device: torch.device,
    chunk_sec: float,
    hop_sec: float,
    activity_threshold: float,
    exist_threshold: float,
    min_active_frames: int,
    prototype_match_threshold: float,
    prototype_momentum: float,
) -> List[Dict[str, Any]]:
    wav = load_wav_mono(wav_path, trim_silence=False)
    feat = wav_to_fbank_infer(wav, crop_sec=None, crop_mode="none").float()

    total_frames = int(feat.size(0))
    chunk_frames = int(round(chunk_sec * 100.0))
    hop_frames = max(1, int(round(hop_sec * 100.0)))
    starts = sliding_window_starts(total_frames, chunk_frames, hop_frames)

    global_tracks = torch.zeros(total_frames, 0, dtype=torch.float32)
    prototypes: List[torch.Tensor] = []

    for start_frame in starts:
        end_frame = min(total_frames, start_frame + chunk_frames)
        chunk_feat = feat[start_frame:end_frame]
        chunk_feat, valid_mask = pad_chunk(chunk_feat, chunk_frames)

        chunk_feat = chunk_feat.unsqueeze(0).to(device)
        valid_mask = valid_mask.unsqueeze(0).to(device)
        _, attractors, exist_logits, diar_logits = model(chunk_feat, valid_mask=valid_mask)

        attractors = attractors[0].cpu()
        exist_logits = exist_logits[0].cpu()
        diar_logits = diar_logits[0].cpu()
        valid_len = int(valid_mask[0].sum().item())

        pred_bin, keep_mask = decode_chunk(
            diar_logits[:valid_len],
            exist_logits,
            min_active_frames=min_active_frames,
            activity_threshold=activity_threshold,
            exist_threshold=exist_threshold,
        )

        active_query_idx = [idx for idx in torch.where(keep_mask)[0].tolist() if bool(pred_bin[:, idx].any().item())]
        if not active_query_idx:
            continue

        active_attractors = attractors[active_query_idx]
        global_ids, prototypes = match_active_attractors(
            active_attractors=active_attractors,
            prototypes=prototypes,
            threshold=prototype_match_threshold,
            momentum=prototype_momentum,
        )

        global_tracks = ensure_track_capacity(global_tracks, len(prototypes))
        for local_idx, global_id in zip(active_query_idx, global_ids):
            segment = pred_bin[:, local_idx]
            window = global_tracks[start_frame:start_frame + valid_len, global_id]
            global_tracks[start_frame:start_frame + valid_len, global_id] = torch.maximum(window, segment)

    return frame_tracks_to_segments(global_tracks)


def main():
    args = parse_args()
    device = torch.device("cuda" if torch.cuda.is_available() and args.device == "cuda" else "cpu")
    out_dir = Path(args.out_dir)
    pred_rttm_dir = out_dir / "pred_rttm"
    pred_rttm_dir.mkdir(parents=True, exist_ok=True)

    model, _ = build_model_from_ckpt(args.ckpt, device=device)
    manifest_items = load_jsonl(args.manifest)
    recordings = group_recordings(manifest_items)

    summary = []
    der_values = []
    jer_values = []

    for rec in recordings:
        pred_segments = infer_recording(
            model=model,
            wav_path=rec["wav_path"],
            device=device,
            chunk_sec=args.chunk_sec,
            hop_sec=args.hop_sec,
            activity_threshold=args.activity_threshold,
            exist_threshold=args.exist_threshold,
            min_active_frames=args.min_active_frames,
            prototype_match_threshold=args.prototype_match_threshold,
            prototype_momentum=args.prototype_momentum,
        )

        pred_rttm_path = pred_rttm_dir / f"{rec['dataset']}__{rec['recording_id']}.rttm"
        write_rttm(pred_rttm_path, rec["recording_id"], pred_segments)

        record_result = {
            "dataset": rec["dataset"],
            "recording_id": rec["recording_id"],
            "wav_path": rec["wav_path"],
            "ref_rttm_path": rec["rttm_path"],
            "pred_rttm_path": str(pred_rttm_path),
            "num_pred_segments": len(pred_segments),
        }

        ref_segments = parse_rttm(rec["rttm_path"])
        score = score_with_pyannote(rec["recording_id"], ref_segments, pred_segments)
        if score is not None:
            record_result.update(score)
            der_values.append(score["der"])
            jer_values.append(score["jer"])

        summary.append(record_result)
        print(record_result)

    summary_path = out_dir / "summary.json"
    with summary_path.open("w", encoding="utf-8") as f:
        json.dump(
            {
                "records": summary,
                "mean_der": (sum(der_values) / len(der_values)) if der_values else None,
                "mean_jer": (sum(jer_values) / len(jer_values)) if jer_values else None,
            },
            f,
            ensure_ascii=False,
            indent=2,
        )

    print(f"[DONE] summary -> {summary_path}")


if __name__ == "__main__":
    main()
